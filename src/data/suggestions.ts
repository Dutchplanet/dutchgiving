// Parse price range string like "€20-40" to get min price
function getMinPrice(priceRange: string): number {
  const match = priceRange.match(/€(\d+)/);
  return match ? parseInt(match[1], 10) : 0;
}

export function getSuggestionsForPerson(
  ageGroup: string,
  gender: string,
  interests: string[],
  budget?: number
): Suggestion[] {
  return suggestions.filter((suggestion) => {
    // Must match age group
    if (!suggestion.targetAgeGroups.includes(ageGroup as any)) {
      return false;
    }

    // Must match gender (or have empty gender array = all genders)
    if (
      suggestion.targetGenders.length > 0 &&
      !suggestion.targetGenders.includes(gender as any)
    ) {
      return false;
    }

    // If suggestion has target interests, at least one must match (or empty = all interests)
    if (suggestion.targetInterests.length > 0) {
      const hasMatchingInterest = suggestion.targetInterests.some((interest) =>
        interests.includes(interest)
      );
      if (!hasMatchingInterest) {
        return false;
      }
    }

    // Filter by budget - only show suggestions where min price fits in budget
    if (budget !== undefined && budget > 0) {
      const minPrice = getMinPrice(suggestion.priceRange);
      if (minPrice > budget) {
        return false;
      }
    }

    return true;
  });
}
